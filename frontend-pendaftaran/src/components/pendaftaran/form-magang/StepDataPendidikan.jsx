const StepDataPendidikan = ({ dk, surface, txt, sub, inputCls, kategori, form, setForm, onNext, onBack }) => {
  const isMahasiswa = kategori === "mahasiswa";
  const isValid = isMahasiswa
    ? form.npm_nim && form.semester && form.asal_kampus && form.fakultas && form.program_studi
    : form.nisn && form.asal_sekolah && form.kelas && form.jurusan_sekolah;

  const handleNumericChange = (field) => (e) => {
    const onlyDigits = e.target.value.replace(/\D/g, "");
    setForm(p => ({ ...p, [field]: onlyDigits }));
  };

  const focusInputCls = `${inputCls} !mt-0 transition-all duration-200 focus:shadow-[0_0_0_4px_rgba(0,165,236,0.12)]`;

  return (
    <div className={`relative overflow-hidden rounded-2xl border p-6 md:p-8 shadow-xl ${surface}`}>
      {/* Dekorasi blur di pojok */}
      <div className="absolute -right-10 -top-10 h-40 w-40 rounded-full bg-[#00A5EC]/5 blur-3xl pointer-events-none" />
      <div className="absolute -left-16 bottom-0 h-48 w-48 rounded-full bg-[#004F9F]/5 blur-3xl pointer-events-none" />

      <div className="relative flex items-start justify-between gap-3 mb-1">
        <div>
          <h3 className={`text-sm font-extrabold ${txt}`}>Data Pendidikan</h3>
          <p className={`text-xs mt-1 ${sub}`}>
            {isMahasiswa ? "Isi data perkuliahan Anda saat ini. Semua kolom wajib diisi." : "Isi data sekolah Anda saat ini. Semua kolom wajib diisi."}
          </p>
        </div>
        <span className={`shrink-0 inline-flex items-center gap-1.5 text-[10px] font-black uppercase tracking-wide px-3 py-1.5 rounded-full ${
          dk ? "bg-gradient-to-br from-[#00A5EC]/20 to-[#004F9F]/20 text-[#00A5EC] ring-1 ring-[#00A5EC]/20" : "bg-gradient-to-br from-[#00A5EC]/10 to-[#004F9F]/10 text-[#004F9F] ring-1 ring-[#004F9F]/10"
        }`}>
          {isMahasiswa ? <CapIcon /> : <SchoolIcon />}
          {isMahasiswa ? "Mahasiswa" : "Siswa"}
        </span>
      </div>

      <div className={`relative mt-6 rounded-2xl border p-5 md:p-6 ${dk ? "border-white/10 bg-white/[0.02]" : "border-slate-100 bg-slate-50/50"}`}>
        <SectionLabel sub={sub}>{isMahasiswa ? "Informasi Kampus" : "Informasi Sekolah"}</SectionLabel>

        {isMahasiswa ? (
          <div className="grid grid-cols-1 sm:grid-cols-2 gap-5">
            <FieldWithIcon dk={dk} icon={<IdCardIcon />} label="NPM / NIM" wajib>
              <input type="text" inputMode="numeric" placeholder="Contoh: 21106050012" value={form.npm_nim} onChange={handleNumericChange("npm_nim")} required className={focusInputCls} />
            </FieldWithIcon>
            <FieldWithIcon dk={dk} icon={<CalendarSemesterIcon />} label="Semester" wajib>
              <input type="number" min="1" max="14" placeholder="Contoh: 5" value={form.semester} onChange={e => setForm(p => ({ ...p, semester: e.target.value }))} required className={focusInputCls} />
            </FieldWithIcon>
            <FieldWithIcon dk={dk} icon={<BuildingIcon />} label="Asal Kampus" wajib span>
              <input type="text" placeholder="Contoh: Universitas Gadjah Mada" value={form.asal_kampus} onChange={e => setForm(p => ({ ...p, asal_kampus: e.target.value }))} required className={focusInputCls} />
            </FieldWithIcon>
            <FieldWithIcon dk={dk} icon={<CapIcon />} label="Fakultas" wajib>
              <input type="text" placeholder="Contoh: Fakultas Teknik" value={form.fakultas} onChange={e => setForm(p => ({ ...p, fakultas: e.target.value }))} required className={focusInputCls} />
            </FieldWithIcon>
            <FieldWithIcon dk={dk} icon={<BookIcon />} label="Program Studi" wajib>
              <input type="text" placeholder="Contoh: Teknik Informatika" value={form.program_studi} onChange={e => setForm(p => ({ ...p, program_studi: e.target.value }))} required className={focusInputCls} />
            </FieldWithIcon>
          </div>
        ) : (
          <div className="grid grid-cols-1 sm:grid-cols-2 gap-5">
            <FieldWithIcon dk={dk} icon={<IdCardIcon />} label="NISN" wajib>
              <input type="text" inputMode="numeric" placeholder="Contoh: 0051234567" value={form.nisn} onChange={handleNumericChange("nisn")} required className={focusInputCls} />
            </FieldWithIcon>
            <FieldWithIcon dk={dk} icon={<ClassIcon />} label="Kelas" wajib>
              <input type="text" placeholder="Contoh: XII RPL 1" value={form.kelas} onChange={e => setForm(p => ({ ...p, kelas: e.target.value }))} required className={focusInputCls} />
            </FieldWithIcon>
            <FieldWithIcon dk={dk} icon={<SchoolIcon />} label="Asal Sekolah" wajib span>
              <input type="text" placeholder="Contoh: SMK Negeri 1 Ponorogo" value={form.asal_sekolah} onChange={e => setForm(p => ({ ...p, asal_sekolah: e.target.value }))} required className={focusInputCls} />
            </FieldWithIcon>
            <FieldWithIcon dk={dk} icon={<BookIcon />} label="Jurusan" wajib span>
              <input type="text" placeholder="Contoh: Rekayasa Perangkat Lunak" value={form.jurusan_sekolah} onChange={e => setForm(p => ({ ...p, jurusan_sekolah: e.target.value }))} required className={focusInputCls} />
            </FieldWithIcon>
          </div>
        )}
      </div>

      <div className={`relative flex gap-4 pt-6 mt-6 border-t ${dk ? "border-white/10" : "border-slate-100"}`}>
        <button
          type="button"
          onClick={onBack}
          className={`group flex-1 flex items-center justify-center gap-2 rounded-full border py-3 text-xs font-bold cursor-pointer transition-all duration-200 hover:-translate-y-0.5 active:scale-95 active:translate-y-0 ${dk ? "border-white/10 text-slate-400 hover:bg-white/5" : "border-slate-200 text-slate-600 hover:bg-slate-50"}`}
        >
          <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={2.5} stroke="currentColor" className="w-3.5 h-3.5 transition-transform group-hover:-translate-x-0.5">
            <path strokeLinecap="round" strokeLinejoin="round" d="M10.5 19.5 3 12m0 0 7.5-7.5M3 12h18" />
          </svg>
          Kembali
        </button>
        <button
          type="button"
          onClick={onNext}
          disabled={!isValid}
          className="group flex-[2] flex items-center justify-center gap-2 rounded-full bg-gradient-to-r from-[#0B1442] to-[#1E3A8A] py-3 text-xs font-bold text-white shadow-lg disabled:opacity-50 disabled:cursor-not-allowed cursor-pointer transition-all duration-200 hover:shadow-xl hover:brightness-110 hover:-translate-y-0.5 active:scale-95 active:translate-y-0 disabled:hover:shadow-lg disabled:hover:translate-y-0 disabled:hover:brightness-100"
        >
          Lanjutkan
          <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={2.5} stroke="currentColor" className="w-3.5 h-3.5 transition-transform group-hover:translate-x-0.5">
            <path strokeLinecap="round" strokeLinejoin="round" d="M13.5 4.5 21 12m0 0-7.5 7.5M21 12H3" />
          </svg>
        </button>
      </div>
    </div>
  );
};

export default StepDataPendidikan;